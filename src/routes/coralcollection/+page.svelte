<h1>The Coral Collection</h1>

<p>
    The Coral Collection was a very simple contact manager that had an aquatic theme to it. 
    The MySQL database was extremely simple, with only two tables: one for registered users
    and one for contacts added by users. RESTful calls to the database were done using PHP.
    I was in charge of the database, and made some of the APIs.
</p>

