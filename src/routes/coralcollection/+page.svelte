<h1>The Coral Collection</h1>

<p>
    As briefly described in the card, The Coral Collection was a very simple contact manager that had
    an aquatic theme to it. The MySQL database was extremely simple, with only two tables: one for registered users
    and one for contacts added by users. RESTful calls to the database were done through PHP,
    almost exclusively using the POST keyword
