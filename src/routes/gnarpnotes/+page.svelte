<h1>Gnarp Notes</h1>

<p>
    Gnarp Notes is a study tool that takes in PDF files from users and converts them into flash cards
    using the Google GeminiAPI, with an option to download the generated flash cards as a PowerPoint
    to use to study later. This was a solo hackathon project that I created during Gemiknights, an AI focused
    hackathon hosted by Knight Hacks at UCF. The overall design of the project went through a couple
    iterations, but the core theme of Gnarpy the alien cat stayed the same. Converting the flash cards 
    into a downloadable PowerPoint was the most challenging part of the project, since finding and 
    implementing the library I used (pptxgenjs) took most of the time I had.
</p>