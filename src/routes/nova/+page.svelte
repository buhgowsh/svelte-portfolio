<h1>N.O.V.A. AI</h1>

<p>
    Neural Optimization with Visual Analysis, or NOVA for short, was a group hackathon project at Hack USF,
    an AI-focused hackathon. NOVA will tracks the user's attention to their screen and graph it by using a 
    combination of OpenCV and the Matplotlib Python library. The graph is then fed to ChatGPT using the OpenAI API
    to gain insight on how well the user payed attention and how to improve attention if needed.
</p>

<p>
    My contribution to this project was setting up the graph using Matplotlib, and setting up a Python VM
    for a groupmate to use Flask, which had been giving them some trouble.
</p>
